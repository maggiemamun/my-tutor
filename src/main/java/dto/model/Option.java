package dto.model;

public class Option {
	private int optNumber;
	private String optDescription;
	private int optionScore;

	public int getOptNumber() {
		return optNumber;
	}

	public void setOptNumber(int optNumber) {
		this.optNumber = optNumber;
	}

	public String getOptDescription() {
		return optDescription;
	}

	public void setOptDescription(String optDescription) {
		this.optDescription = optDescription;
	}

	public int getOptionScore() {
		return optionScore;
	}

	public void setOptionScore(int optionScore) {
		this.optionScore = optionScore;
	}

}
